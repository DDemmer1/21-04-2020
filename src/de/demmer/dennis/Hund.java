package de.demmer.dennis;

public class Hund {

	private String name;
	private int alter;
	
	
	public Hund(String name, int alter) {
		super();
		this.name = name;
		this.alter = alter;
	}
	
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getAlter() {
		return alter;
	}
	public void setAlter(int alter) {
		this.alter = alter;
	}

	
	
	
	
	
	
}
